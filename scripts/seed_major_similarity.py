async def seed():
    engine = create_async_engine(settings.database_url, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as session:
        inserted = 0
        skipped = 0
        for source, target, sim in MAPPINGS:
            try:
                await session.execute(
                    text("""
                        INSERT INTO major_similarity (source_major, target_major, similarity)
                        VALUES (:src, :tgt, :sim)
                        ON DUPLICATE KEY UPDATE similarity = VALUES(similarity)
                    """),
                    {"src": source, "tgt": target, "sim": sim},
                )
                inserted += 1
            except Exception as e:
                print(f"  SKIP {source} → {target}: {e}")
                skipped += 1

        await session.commit()
        print(f"major_similarity: {inserted} upserted, {skipped} skipped")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
