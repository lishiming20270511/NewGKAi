async def seed():
    engine = create_async_engine(settings.database_url, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as session:
        inserted = 0
        skipped = 0
        for row in CITY_DATA:
            city_name, location, advantage, development, main_business, city_level = row
            try:
                await session.execute(
                    text("""
                        INSERT INTO city_analysis (city_name, location, advantage, development, main_business, city_level)
                        VALUES (:city_name, :location, :advantage, :development, :main_business, :city_level)
                        ON DUPLICATE KEY UPDATE
                            location = VALUES(location),
                            advantage = VALUES(advantage),
                            development = VALUES(development),
                            main_business = VALUES(main_business),
                            city_level = VALUES(city_level)
                    """),
                    {
                        "city_name": city_name,
                        "location": location,
                        "advantage": advantage,
                        "development": development,
                        "main_business": main_business,
                        "city_level": city_level,
                    },
                )
                inserted += 1
            except Exception as e:
                print(f"  [跳过] {city_name}: {e}")
                skipped += 1

        await session.commit()
        print(f"\n城市分析种子数据写入完成：{inserted} 条插入/更新，{skipped} 条跳过")
        print(f"总计城市数量：{len(CITY_DATA)}")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
