async def seed():
    engine = create_async_engine(settings.database_url, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as session:
        # 预先加载所有学校名→ID的映射
        rows = await session.execute(text("SELECT id, name FROM schools"))
        school_map = {r["name"]: r["id"] for r in rows.mappings()}
        print(f"数据库共有 {len(school_map)} 所学校")

        emp_ok = emp_skip = 0
        sal_ok = sal_skip = 0

        # 写入就业率数据
        print("\n--- 写入就业率数据 ---")
        for school_name, emp_rate, grad_rate, source, year in EMPLOYMENT_DATA:
            sid = school_map.get(school_name)
            if not sid:
                print(f"  [找不到学校] {school_name}")
                emp_skip += 1
                continue
            try:
                await session.execute(
                    text("""
                        INSERT INTO school_employment (school_id, employment_rate, graduate_rate, data_source, data_year)
                        VALUES (:sid, :er, :gr, :src, :yr)
                        ON DUPLICATE KEY UPDATE
                            employment_rate = VALUES(employment_rate),
                            graduate_rate   = VALUES(graduate_rate),
                            data_source     = VALUES(data_source),
                            data_year       = VALUES(data_year)
                    """),
                    {"sid": sid, "er": emp_rate, "gr": grad_rate, "src": source, "yr": year},
                )
                emp_ok += 1
            except Exception as e:
                print(f"  [错误] {school_name}: {e}")
                emp_skip += 1

        # 写入薪资数据
        print("\n--- 写入薪资数据 ---")
        for school_name, major, s_min, s_max, t_min, t_max, source, year in SALARY_DATA:
            sid = school_map.get(school_name)
            if not sid:
                print(f"  [找不到学校] {school_name}")
                sal_skip += 1
                continue
            try:
                await session.execute(
                    text("""
                        INSERT INTO school_salary
                            (school_id, major_name, salary_start_min, salary_start_max,
                             salary_3yr_min, salary_3yr_max, data_source, data_year)
                        VALUES (:sid, :major, :s_min, :s_max, :t_min, :t_max, :src, :yr)
                        ON DUPLICATE KEY UPDATE
                            salary_start_min = VALUES(salary_start_min),
                            salary_start_max = VALUES(salary_start_max),
                            salary_3yr_min   = VALUES(salary_3yr_min),
                            salary_3yr_max   = VALUES(salary_3yr_max),
                            data_source      = VALUES(data_source),
                            data_year        = VALUES(data_year)
                    """),
                    {
                        "sid": sid, "major": major,
                        "s_min": s_min, "s_max": s_max,
                        "t_min": t_min, "t_max": t_max,
                        "src": source, "yr": year,
                    },
                )
                sal_ok += 1
            except Exception as e:
                print(f"  [错误] {school_name}-{major}: {e}")
                sal_skip += 1

        await session.commit()
        print(f"\n就业率数据：{emp_ok} 条写入，{emp_skip} 条跳过")
        print(f"薪资数据：  {sal_ok} 条写入，{sal_skip} 条跳过")
        print(f"合计：{emp_ok + sal_ok} 条成功写入")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
