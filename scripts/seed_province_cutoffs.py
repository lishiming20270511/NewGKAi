async def seed() -> None:
    engine = create_async_engine(settings.database_url, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    inserted = skipped = 0
    async with async_session() as session:
        for province, subject_category, cutoff_yiben, cutoff_zhuanke in CUTOFF_DATA_2025:
            try:
                await session.execute(
                    text("""
                        INSERT INTO province_cutoffs
                            (province, subject_category, year, cutoff_yiben, cutoff_zhuanke)
                        VALUES
                            (:province, :subject_category, 2025, :cutoff_yiben, :cutoff_zhuanke)
                        ON DUPLICATE KEY UPDATE
                            cutoff_yiben   = VALUES(cutoff_yiben),
                            cutoff_zhuanke = VALUES(cutoff_zhuanke),
                            updated_at     = NOW()
                    """),
                    {
                        "province": province,
                        "subject_category": subject_category,
                        "cutoff_yiben": cutoff_yiben,
                        "cutoff_zhuanke": cutoff_zhuanke,
                    },
                )
                inserted += 1
            except Exception as e:
                print(f"  SKIP {province}/{subject_category}: {e}")
                skipped += 1

        await session.commit()
        print(f"province_cutoffs 2025: inserted/updated {inserted} rows, skipped {skipped}")

    r = await session.execute(
        text("SELECT COUNT(*) FROM province_cutoffs WHERE year=2025")
    )
    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
